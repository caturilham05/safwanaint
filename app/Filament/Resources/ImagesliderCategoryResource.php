<?php

namespace App\Filament\Resources;

use App\Filament\Resources\ImagesliderCategoryResource\Pages;
use App\Filament\Resources\ImagesliderCategoryResource\RelationManagers;
use App\Models\ImagesliderCategory;
use Filament\Forms;
use Filament\Forms\Form;
use Filament\Resources\Resource;
use Filament\Tables;
use Filament\Tables\Table;
use Illuminate\Database\Eloquent\Builder;
use Illuminate\Database\Eloquent\SoftDeletingScope;

class ImagesliderCategoryResource extends Resource
{
    protected static ?string $model = ImagesliderCategory::class;

    protected static ?string $navigationIcon = 'heroicon-o-rectangle-stack';

    public static function form(Form $form): Form
    {
        return $form
            ->schema([
                Forms\Components\TextInput::make('name')->maxLength(50),
            ]);
    }

    public static function table(Table $table): Table
    {
        return $table
            ->columns([
                Tables\Columns\TextColumn::make('name')->searchable(),
            ])
            ->filters([
                //
            ])
            ->actions([
                Tables\Actions\EditAction::make(),
            ])
            ->bulkActions([
                Tables\Actions\BulkActionGroup::make([
                    Tables\Actions\DeleteBulkAction::make(),
                ]),
            ]);
    }

    public static function getRelations(): array
    {
        return [
            //
        ];
    }

    public static function getPages(): array
    {
        return [
            'index' => Pages\ListImagesliderCategories::route('/'),
            'create' => Pages\CreateImagesliderCategory::route('/create'),
            'edit' => Pages\EditImagesliderCategory::route('/{record}/edit'),
        ];
    }
}
